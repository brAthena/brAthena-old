/* Banco de dados alteração de estado(SC)

   Estrutura do banco de dados:
   SC_NAME,flag
   
   flag    0x1  - SC não pode ser removido por morte.
           0x2  - SC não pode ser salvo.
           0x4  - SC não pode ser afetado por desencantar.
           0x8  - SC não pode ser afetado por Gênese.
           0x10 - SC é considerado como buff e pode ser removido por Bastão de Hermod, etc
           0x20 - SC é considerado como debuff e pode ser removido por Canto de Batalha, etc.
           0x40 - SC não pode ser redefinida quando M.E.C.H.A. é retirado.*/

